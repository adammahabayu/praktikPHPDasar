<?php
// Menghitung keliling lingkaran dengan jari-jari 15 cm
$radius = 15; // Jari-jari lingkaran dalam cm
$pi = 3.14; // Nilai phi (π)

// Menghitung keliling lingkaran menggunakan rumus K = 2 * π * r
$keliling = 2 * $pi * $radius;

// Menampilkan pesan dengan format yang berbeda
echo "Jari-jari lingkaran: $radius cm <br>";
echo "Nilai phi (π): $pi <br>";
echo "Keliling lingkaran dengan jari-jari $radius cm adalah $keliling cm.";
?>
